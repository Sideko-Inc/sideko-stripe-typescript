import Stripe, { Environment } from "@sideko-inc/stripe";

describe("tests client.issuing.token.list", () => {
  test.concurrent(
    "GET /v1/issuing/tokens | testId: generated_success | Success test with response schema validation. Expects status code 200",
    async () => {
      const client = new Stripe({
        token: "API_TOKEN",
        environment: Environment.MockServer,
      });
      // Get both raw response for status and parsed response for data
      const [rawResponse, response] = await Promise.all([
        client.issuing.token.list({ card: "string" }).asResponse(),
        client.issuing.token.list({ card: "string" }),
      ]);
      expect(rawResponse.status).toBe(200); // Exact status code match
      // Response body automatically validated by Zod schema during deserialization
      expect(response).toBeDefined();
    },
  );
});

